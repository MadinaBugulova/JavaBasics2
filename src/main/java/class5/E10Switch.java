package class5;

public class E10Switch {
    public static void main(String[] args) {


        int day=10;


        switch (day) {
            case 1:
                System.out.println("Monday");
                break;
            case 2:
                System.out.println("Tuesday");
                break;
            case 3:
                System.out.println("Wednesday");
                break;
            case 5:
                System.out.println("Thursday");
                break;
            default:
                System.out.println("Wrong day");
        }
    }
}
