package class5;

public class E11Switch {
    public static void main(String[] args) {
        int day=10;


        if (day == 2) {
            System.out.println("Tuesday");
        } else if (day == 3) {
            System.out.println("Wednesday");
        } else if (day == 5) {
            System.out.println("Thursday");
        } else {
            System.out.println("Wrong day");
        }
    }
}


